package lecture.hashing.hashOpenAddress;

public class HashTableOpenAddressingMain {

	public static void main(String[] args) {
		HashTableOpenAddressing hash = new HashTableOpenAddressing(5);
		hash.put(1, "A");
		hash.put(3, "B");
		System.out.println(hash);
		System.out.println(hash.size());
		hash.put(6, "C");
		hash.put(11, "D");
		System.out.println(hash);
		System.out.println(hash.size());
		hash.put(11, "A");
		hash.put(16, "E");
		hash.put(16, "E");
		System.out.println(hash);
		System.out.println(hash.size());
		System.out.println(hash.get(3));
		hash.put(16, "F");
		System.out.println(hash);
		System.out.println(hash.size());
		hash.remove(16);		
		System.out.println(hash);
		System.out.println(hash.size());
		hash.remove(1);
		System.out.println(hash);
		System.out.println(hash.size());
		System.out.println(hash.get(1));
		hash.put(31, "W");
		hash.put(41, "Y");
		System.out.println(hash);
		hash.put(41, "Z");
		System.out.println(hash);
		System.out.println(hash.size());
	}

}
